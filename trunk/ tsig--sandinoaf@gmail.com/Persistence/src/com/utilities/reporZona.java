package com.utilities;

public class reporZona {

	public reporZona(){}
	
	public Integer idZona;
	public String desc;
	public Integer importancia;
	public Integer cantIncdentes;
	
	public Integer getIdZona() {
		return idZona;
	}
	public void setIdZona(Integer idZona) {
		this.idZona = idZona;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public Integer getImportancia() {
		return importancia;
	}
	public void setImportancia(Integer importancia) {
		this.importancia = importancia;
	}
	public Integer getCantIncdentes() {
		return cantIncdentes;
	}
	public void setCantIncdentes(Integer cantIncdentes) {
		this.cantIncdentes = cantIncdentes;
	}
}
